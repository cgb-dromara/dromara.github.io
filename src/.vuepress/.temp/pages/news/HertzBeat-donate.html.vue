<h2 id="what-is-hertzbeat" tabindex="-1"><a class="header-anchor" href="#what-is-hertzbeat"><span>What is HertzBeat</span></a></h2>
<p>Apache HertzBeat(Incubating) is an easy-to-use and friendly open source real-time monitoring and alarm system. It does not require an Agent, has a high-performance cluster, is compatible with Prometheus, and provides powerful custom monitoring and status page construction capabilities.</p>
