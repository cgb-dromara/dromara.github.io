<li>
<p>Configurable protocol specifications such as 'Http, Jmx, Ssh, Snmp, Jdbc, and Prometheus'. You only need to configure the monitoring template 'YML' in the browser to use these protocols to customize the collection of desired indicators. Do you believe that new monitoring types such as 1 'K8s' or 'Docker' can be immediately adapted just by configuration?</p>
</li>
<li>
<p>Compatible with the' Prometheus' system ecology and more, only page operations can monitor what 'Prometheus' can monitor.</p>
</li>
